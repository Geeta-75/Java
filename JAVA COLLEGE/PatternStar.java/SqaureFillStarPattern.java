public class SqaureFillStarPattern {

    public static void main(String args [] )

    {
       int i ,row = 6;
       int j,  col =6;
       
       for (i = 1; i<=row; i++)
       {
        for ( j=1; j<=col; j++)
        {
            System.out.print(" * ");
        }
        System.out.println();
       }
    }
    
}
